SELECT (SELECT NAME 
                FROM CUSTOMER CS 
                WHERE CS.CUSTID = OD.CUSTID) "NAME", SUM(SALEPRICE) "TOTAL"
FROM ORDERS OD
GROUP BY OD.CUSTID;

SELECT NAME, SUM(SALEPRICE)
FROM ORDERS OD, CUSTOMER CS
WHERE OD.CUSTID = CS.CUSTID
GROUP BY NAME;

