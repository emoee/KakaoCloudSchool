-- 1. 비상관 서브 질의
SELECT ORDERID, SALEPRICE
FROM ORDERS
WHERE SALEPRICE <= (SELECT AVG(SALEPRICE)
                                            FROM ORDERS);

-- 2. 상관 서브 질의
SELECT ORDERID, CUSTID, SALEPRICE
FROM ORDERS OD
WHERE SALEPRICE > (SELECT AVG(SALEPRICE)
                                            FROM ORDERS SOD
                                            WHERE OD.CUSTID = SOD.CUSTID);
